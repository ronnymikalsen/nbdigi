import { NgModule } from '@angular/core';
import { TestBed } from '@angular/core/testing';
import { readFirst } from '@nrwl/nx/testing';

import { EffectsModule } from '@ngrx/effects';
import { StoreModule, Store } from '@ngrx/store';

import { NxModule } from '@nrwl/nx';

import { ItemEffects } from './item.effects';
import { ItemFacade } from './item.facade';

import { itemQuery } from './item.selectors';
import { LoadItem, ItemLoaded } from './item.actions';
import { ItemState, Entity, initialState, itemReducer } from './item.reducer';

interface TestSchema {
  item: ItemState;
}

describe('ItemFacade', () => {
  let facade: ItemFacade;
  let store: Store<TestSchema>;
  let createItem;

  beforeEach(() => {
    createItem = (id: string, name = ''): Entity => ({
      id,
      name: name || `name-${id}`
    });
  });

  describe('used in NgModule', () => {
    beforeEach(() => {
      @NgModule({
        imports: [
          StoreModule.forFeature('item', itemReducer, { initialState }),
          EffectsModule.forFeature([ItemEffects])
        ],
        providers: [ItemFacade]
      })
      class CustomFeatureModule {}

      @NgModule({
        imports: [
          NxModule.forRoot(),
          StoreModule.forRoot({}),
          EffectsModule.forRoot([]),
          CustomFeatureModule
        ]
      })
      class RootModule {}
      TestBed.configureTestingModule({ imports: [RootModule] });

      store = TestBed.get(Store);
      facade = TestBed.get(ItemFacade);
    });

    /**
     * The initially generated facade::loadAll() returns empty array
     */
    it('loadAll() should return empty list with loaded == true', async done => {
      try {
        let list = await readFirst(facade.allItem$);
        let isLoaded = await readFirst(facade.loaded$);

        expect(list.length).toBe(0);
        expect(isLoaded).toBe(false);

        facade.loadAll();

        list = await readFirst(facade.allItem$);
        isLoaded = await readFirst(facade.loaded$);

        expect(list.length).toBe(0);
        expect(isLoaded).toBe(true);

        done();
      } catch (err) {
        done.fail(err);
      }
    });

    /**
     * Use `ItemLoaded` to manually submit list for state management
     */
    it('allItem$ should return the loaded list; and loaded flag == true', async done => {
      try {
        let list = await readFirst(facade.allItem$);
        let isLoaded = await readFirst(facade.loaded$);

        expect(list.length).toBe(0);
        expect(isLoaded).toBe(false);

        store.dispatch(new ItemLoaded([createItem('AAA'), createItem('BBB')]));

        list = await readFirst(facade.allItem$);
        isLoaded = await readFirst(facade.loaded$);

        expect(list.length).toBe(2);
        expect(isLoaded).toBe(true);

        done();
      } catch (err) {
        done.fail(err);
      }
    });
  });
});
